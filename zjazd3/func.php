<?php
function addNum(int $first_num, int $second_num): int
{
    return $first_num + $second_num;
}

function subNum(int $first_num, int $second_num): int
{
    return $first_num - $second_num;
}

function mulNum(int $first_num, int $second_num): int
{
    return $first_num * $second_num;
}

function divNum(int $first_num, int $second_num): int
{
    return $first_num / $second_num;
}

